//arithmetic_calcs

#include<stdio.h>
#include<math.h>

void main()
{
    float x;
    printf("Enter a real number of x: ");
    scanf("%f",&x);
    
    printf("The tangential value of x = %f\n", tan(x));
    printf("The logarithmic value of x = %f\n", log(x));
    printf("The square root of x = %f\n", sqrt(x));
}
